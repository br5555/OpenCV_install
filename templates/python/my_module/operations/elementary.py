"""
Elementary arithmetic is the simplified portion of arithmetic that includes the
 operations of addition, subtraction, multiplication, and division. It should 
 not be confused with elementary function arithmetic.
"""

def add(a, b):
    """
    This function return the sum of two numbers: ``a`` + ``b``.
    """    
    return a+b

def subtract(a, b):
    """
    This function return the subtraction of two numbers: ``a`` - ``b``.
    """
    return a-b
    
def multiply(a, b):
    """
    This function return the multiplication of two numbers: ``a`` * ``b``.
    """
    return a*b
    
def divide(a, b):
    """
    This function return the division of two numbers: ``a`` / ``b``.
    """
    return a/b